// now draws outline of BoundingCircle around Goat entity
Circle.prototype.draw = function (ctx) {
    ctx.beginPath();
    ctx.strokeStyle = "purple";
    ctx.arc(this.x, this.y, this.radius, 0, Math.PI * 2, false);
    ctx.stroke();
    ctx.closePath();
};